
public class Intliteras {

	public static void main(String[] args) {
		System.out.println(1066);//simple literal
		System.out.println(-1066);
		System.out.println(11);
		System.out.println(011);//put zero before num it consider as octal literal
		System.out.println(017);//(0-7)
		System.out.println(0x11AB);//hexa decimal literal(0-15)
		System.out.println(0b11);//binary literal
	}

}
